function translateWithSpaces(lang: AppLanguage, key: string): string {
  const table = dict[lang] || dict.ru;
  if (Object.prototype.hasOwnProperty.call(table, key)) return table[key];
  const match = key.match(/^(\s*)(.*?)(\s*)$/s);
  if (match && match[2] && Object.prototype.hasOwnProperty.call(table, match[2])) return `${match[1]}${table[match[2]]}${match[3]}`;
  return key;
}


export function translateNow(key: string): string {
  return translateWithSpaces((mem[globalKey] || 'ru') as AppLanguage, key);
}

const originalAlertKey = '__AGASHKA_ORIGINAL_ALERT__';
function installTranslatedAlert() {
  if (!mem[originalAlertKey]) mem[originalAlertKey] = Alert.alert.bind(Alert);
  const originalAlert = mem[originalAlertKey];
  Alert.alert = ((title: any, message?: any, buttons?: any, options?: any) => {
    const translatedButtons = Array.isArray(buttons)
      ? buttons.map((b) => ({ ...b, text: typeof b?.text === 'string' ? translateNow(b.text) : b?.text }))
      : buttons;
    return originalAlert(
      typeof title === 'string' ? translateNow(title) : title,
      typeof message === 'string' ? translateNow(message) : message,
      translatedButtons,
      options
    );
  }) as any;
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [languageState, setLanguageState] = useState<AppLanguage>(mem[globalKey] || 'ru');
  useEffect(() => { installTranslatedAlert(); }, [languageState]);
  const api = useMemo<I18nApi>(() => ({
    language: languageState,
    setLanguage(lang: AppLanguage) { mem[globalKey] = lang; setLanguageState(lang); },
    t(key: string) { return translateWithSpaces(languageState, key); },
  }), [languageState]);
  return <I18nContext.Provider value={api}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('I18nProvider is missing');
  return ctx;
}
